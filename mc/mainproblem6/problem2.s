;b. ALP to generate the first n Fibonacci numbers
	AREA PROB2,CODE,READONLY
TERM RN 1
NEXT RN 2
COUNT RN 3
PLACE RN 4

ENTRY
	LDR PLACE,=TABLE
	MOV TERM,#1
	MOV NEXT,#1
	MOV COUNT,#10
	STR TERM,[PLACE],#4
	STR TERM,[PLACE],#4
	SUB COUNT,#2

LOOP
	ADD NEXT,NEXT,TERM
	SUB TERM,NEXT,TERM
	STR NEXT,[PLACE],#4
	SUBS COUNT,#1
	BNE LOOP
GO B GO
	
	AREA FIBONACCI,DATA,READWRITE
TABLE SPACE 20
	END