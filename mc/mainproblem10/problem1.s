;ARM ASSEMBLY PROGRAM TO FIND WHETHER A NUMBER IS A PALINDROME 
	AREA PROB1,CODE,READONLY
;the result will be stored in d as either 0 or 1
NUM RN 1
R RN 2
DD RN 3
Q RN 5
d RN 0
ENTRY
	; CHANGE THE VALUE IN 0X40000000 TO LOAD THE NUBMER INTO THE PROGRAM
	;---------------------
	;MOV NUM,#0x40000000
	;LDR NUM,[NUM]
	;-----------------
	;---------------
	LDR NUM,=1223221
	;-----------------
	MOV R,#0
	MOV Q,NUM
	mov d,#10
	MOV DD,#0
LOOP
	BL DIVIDE
	MLA R,d,R,DD
	CMP Q,#0
	BNE LOOP ; IF R LESSER THAN NUM CONTINUE THE REVERSAL
	BEQ FINAL
DIVIDE
	MOV DD,Q
	MOV Q,#0
DIVLOOP
	SUBS DD,#10
	ADDPL Q,#1
	ADDMI DD,#10
	BPL DIVLOOP
	BX LR
FINAL 
	CMP R,NUM
	MOVEQ d,#1
	MOVNE d,#0
GO B GO
	END