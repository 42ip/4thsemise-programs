;a. ALP to count the number of zeroes and ones in a binary number.
	AREA PROB1,CODE,READONLY

NUMBER RN 1
ZEROS RN 2
ONES RN 3 
PLACE RN 5
ENTRY
	MOV NUMBER,#134
	MOV ZEROS,#0
	MOV ONES ,#0
	MOV PLACE,#0X40000000
LOOP
	LSRS NUMBER,#1
	ADDCS ONES,#1 ; HERE WE CHECK IF THE CARRY BIT IS ACTIVATED.
	ADDCC ZEROS,#1 ; HERE WE CHECK IF THE CARRY BIT IS NOT SWITCHED.
	BNE LOOP
	STR ONES,[PLACE],#4
	STR ZEROS,[PLACE]
GO B GO
	END